package com.alroid.fragmentsample.entity;

import android.graphics.drawable.Drawable;

public class Item {
    private int id;
    private Drawable img;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Drawable getImg() {
        return img;
    }

    public void setImg(Drawable img) {
        this.img = img;
    }
}
